import turtle
t=turtle.Turtle()
s=turtle.Screen()

s.bgcolor("black")
t.speed(1)
t.hideturtle()
t.goto(0,-10)

t.pensize(3)
t.color("red")
t.begin_fill()
t.left(140)
t.forward(180)
t.circle(-90,200)
t.setheading(60)
t.circle(-90,200)
t.forward(178)
t.end_fill()

t.penup()
t.goto(-120,130)
t.pendown()
t.color("white")
t.write("te amo hermanito <3", font= ("Verdana", 17, "bold"))


t.penup()
t.goto(-75,60)
t.pendown()
t.color("white")
t.write("feliz cumpleaños :)", font=("Verdana", 12, "bold"))


